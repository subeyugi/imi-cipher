-- 重複テーブル削除用
DROP TABLE IF EXISTS composers;
DROP TABLE IF EXISTS decipherers;
DROP TABLE IF EXISTS cipher_methods;
DROP TABLE IF EXISTS songs;
DROP TABLE IF EXISTS decode_posts;

-- テーブル作成
CREATE TABLE IF NOT EXISTS composers (                      -- 作曲者、暗号作成者リスト
    id                  INTEGER PRIMARY KEY, 
    user_name           TEXT,                                   -- 名前
    song_ids            TEXT);                                  -- 曲のid、複数の場合は"-"区切り

CREATE TABLE IF NOT EXISTS decipherers (                    -- 解読者リスト
    id                  INTEGER PRIMARY KEY,
    user_name           TEXT,                                   -- 名前
    post_ids            TEXT);                                  -- 解読ポストのid、複数の場合は"-"区切り

CREATE TABLE IF NOT EXISTS cipher_methods ( 
    id                  INTEGER PRIMARY KEY, 
    method_name         TEXT, 
    post_ids            TEXT);

CREATE TABLE IF NOT EXISTS songs (          
    id                  INTEGER PRIMARY KEY, 
    title               TEXT, 
    composer_ids        TEXT, 
    release_date        TEXT, 
    motif               TEXT);

CREATE TABLE IF NOT EXISTS decode_posts (   
    id                  INTEGER PRIMARY KEY, 
    song_id             INTEGER, 
    decipherer_ids      INTEGER, 
    deciphering_date    TEXT, 
    post_date           TEXT, 
    tag_ids             TEXT);

